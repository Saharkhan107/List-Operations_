# Create a list of integers
numbers = [10, 5, 8, 3, 7]

# Append a number
numbers.append(12)

# Insert a number at index 2
numbers.insert(2, 6)

# Sort the list
numbers.sort()

# Pop the last element and print it
print("Popped:", numbers.pop())

# Remove a specific number (8)
numbers.remove(8)

print("Final list:", numbers)


